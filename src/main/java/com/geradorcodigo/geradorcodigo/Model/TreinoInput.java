package com.geradorcodigo.geradorcodigo.Model;

public class TreinoInput {

        private int id;
        private String nome;
        private int exercicio;
        private int aluno;
        private String repeticoes;
        private String velocidade;
        private String descanso;
        private int musculoAlvo;
        private int diaSemana;
        private String series;
        private int objetivo;

        public int getId() {
                return id;
        }
        public void setId(int id) {
                this.id = id;
        }
        public String getNome() {
                return nome;
        }
        public void setNome(String nome) {
                this.nome = nome;
        }
        public int getExercicio() {
                return exercicio;
        }
        public void setExercicio(int exercicio) {
                this.exercicio = exercicio;
        }
        public int getAluno() {
                return aluno;
        }
        public void setAluno(int aluno) {
                this.aluno = aluno;
        }
        public String getRepeticoes() {
                return repeticoes;
        }
        public void setRepeticoes(String repeticoes) {
                this.repeticoes = repeticoes;
        }
        public String getVelocidade() {
                return velocidade;
        }
        public void setVelocidade(String velocidade) {
                this.velocidade = velocidade;
        }
        public String getDescanso() {
                return descanso;
        }
        public void setDescanso(String descanso) {
                this.descanso = descanso;
        }
        public int getMusculoAlvo() {
                return musculoAlvo;
        }
        public void setMusculoAlvo(int musculoAlvo) {
                this.musculoAlvo = musculoAlvo;
        }
        public int getDiaSemana() {
                return diaSemana;
        }
        public void setDiaSemana(int diaSemana) {
                this.diaSemana = diaSemana;
        }
        public String getSeries() {
                return series;
        }
        public void setSeries(String series) {
                this.series = series;
        }
        public int getObjetivo() {
                return objetivo;
        }
        public void setObjetivo(int objetivo) {
                this.objetivo = objetivo;
        }
        
        
}